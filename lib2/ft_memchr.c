#include "libft.h"

void	*ft_memchr(const void *buf, int c, size_t n)
{
	size_t			i;
	unsigned char	*src;

	src = (unsigned char *)buf;
	i = 0;
	while (i < n)
	{
		if (src[i] == (unsigned char)c)
			return (src + i);
		i++;
	}
	return (NULL);
}
