#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	i;
	char	*dup;
	size_t	len;

	i = 0;
	len = ft_strlen(s) + 1;
	dup = malloc(len * sizeof(char));
	if (!dup)
		return (NULL);
	while (s[i] != '\0')
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
